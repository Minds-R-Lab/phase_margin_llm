#!/usr/bin/env python3
"""
run_embed_validation.py
=======================

Path-beta validation harness:  certifies an LLM paraphrase loop using
**direct embedding-space perturbation injection** through a HuggingFace
``transformers`` model loaded locally.  This is the operation §5.1 of
the paper actually defines:

  - perturbation u_k = epsilon * v * cos(Omega * k) is added to the
    last-input-token embedding via a forward hook on the embedding layer,
  - observable = the same model's last-hidden-state at the same token,
  - probe basis = top-N principal directions of the loop's nominal
    unperturbed trajectory in that hidden-state space.

The text-modifier channel (used by ``run_extensive_validation.py``) is
not used here.  Same per-cell artefacts (raw trajectories, probe
results, permutation null distributions, hypothesis evaluation) are
saved alongside ``experiment_*`` directories generated by the text
harness, so the two can be compared row-by-row.

Usage
-----
    python experiments/run_embed_validation.py
    python experiments/run_embed_validation.py --model "Qwen/Qwen2.5-7B-Instruct" --quick
    python experiments/run_embed_validation.py --prompts factual --probe-seeds 3 --tag beta_v1
"""
